// program for simple arthmatic operators

#include<stdio.h>

int main()
{
	int a = 5;
	int b = 10; 
	int c = 0;
	
	c = a + b;
	printf("Addition is %d \n", c);
	
	c = a - b;
	printf("Substration is %d \n", c);
	
	c = a * b;
	printf("Multiplication is %d \n", c);
	
	c = a / b;
	printf("Divison is %d \n", c);
	
	c = a % b;
	printf("Remainder is %d \n", c);
	
	return 0;
}





